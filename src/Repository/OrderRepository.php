    public function findPaidOrder(User $user)
    {
        $queryBuilder = $this->createQueryBuilder('o')
            ->where('o.user = :user')
            ->andWhere('o.status = 1')
            ->orderBy('o.id', 'desc')
            ->setParameter('user', $user);

        $query = $queryBuilder->getQuery();
        
        return $query->getResult();
    }

